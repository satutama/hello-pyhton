#Conditional execution & Comparison operators

x = 5
if x == 5:
    print('Equals 5')
if x > 4:
    print('Greater than 4')
if x >= 5:
    print('Greater than or equal 5')
if x < 6:
    print('Less than 6')
if x <= 5:
    print('Less than or equal 5')
if x != 6:
    print('Not equal 6')


